package dipl_project.Roads;

/**
 *
 * @author Honza
 */
public class MyPoint {
    private double x, y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public MyPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }
    
}
